def progress_bar_wolf(task, cmd, color):
    width = 30
    spin_chars = ['‚†ã', '‚†ô', '‚†π', '‚†∏', '‚†º', '‚†¥', '‚†¶', '‚†ß', '‚†á', '‚†è']
    spin_idx = 0
    progress = 0

    print(f"{color}{task}...{reset} ", end='', flush=True)
    proc = subprocess.Popen(cmd, shell=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    while proc.poll() is None:
        progress = min(progress + random.randint(1, 3), 100)
        filled = int(width * progress / 100)
        empty = width - filled
        # Aqui, adicione o c√≥digo de cor verde na barra preenchida:
        bar_filled = f"{green}{'‚ñà' * filled}{reset}"
        bar_empty = '-' * empty
        spinner = spin_chars[spin_idx]
        print(f"\r{color}{task}...{reset} {spinner} [{bar_filled}{bar_empty}] {progress}%", end='', flush=True)
        spin_idx = (spin_idx + 1) % len(spin_chars)
        time.sleep(0.1)
    proc.wait()
    print(f"\r{color}{task}...{reset} ‚úî [{green}{'‚ñà'*width}{reset}] 100% {green}[Success]{reset}")
    return proc.returncode


