# Fun√ß√£o barra de progresso colorida
progress_bar() {
  local task="$1"
  local cmd="$2"
  local color="$3"
  local cols=30
  local i=0
  local delay=0.08

  echo -e "${color}${task}...${reset}"

  bash -c "$cmd" &> /dev/null &
  local pid=$!

  while kill -0 $pid 2> /dev/null; do
    i=$(( (i+1) % (cols+1) ))
    local filled=$i
    local empty=$((cols - filled))
    local bar="$(printf '%0.s‚ñà' $(seq 1 $filled))$(printf '%0.s-' $(seq 1 $empty))"
    printf "\r[${green}%s${reset}] %3d%%" "$bar" $((i*100/cols))
    sleep $delay
  done

  wait $pid
  local status=$?
  if [ $status -eq 0 ]; then
    printf "\r[${green}$(printf '%0.s‚ñà' $(seq 1 $cols))${reset}] 100%% ${green}[Sucesso]${reset}\n"
  else
    printf "\r[${red}$(printf '%0.s‚ñà' $(seq 1 $cols))${reset}] 100%% ${red}[Falha]${reset}\n"
    exit 1
  fi
}

